#defining functions
def hello():
  print('Hello, good morning!')
def aboutme():
  print('My name is Aarav,')
  print('my age is 11,')
  print('and my favorite color is red.')
#functions with parameters
def aboutme2(a,b,c):
  print('Remember, my name is'+a+',')
  print('my age is'+b+',')
  print('and my favorite color is'+c+'!')
def hello2(a):
  print(a)
#calling functions
hello()
aboutme()
#calling functions with parameters
hello2('Goodbye,')
hello2('have a good day,')
hello2('and have fun!')
aboutme2(' Aarav',' 11',' red')
aboutme2(' Sia',' 11',' blue')